// Premitive Data types
// String 
console.log('Hello');

// String concatenation
let name = 'Muhammad';
console.log('Hello' + ' ' + name);

// typeof return the data type
console.log('Data type is ' + typeof(name));

// Number 
let age = 18;
console.log(age);
console.log('Data type is ' + typeof age);

// Boolean 
let isDriver = true;
console.log(isDriver);
console.log('Data type is ' + typeof isDriver);

// null 
let nullVar = null;
console.log(nullVar);
console.log('Data type is ' + typeof nullVar);

// null 
let undef = undefined;
console.log(undef);
console.log('Data type is ' + typeof undef);

// Reference Data types

// Array
let arr = [1, 2, 3, false, 'moon', [1, 11, 111], {moon: 27, mahadi: 8}];
console.log(arr);
console.log('Data type is ' + typeof arr);

// Object Literals
let mathMarks = {
    Rohan: 33,
    'Harry Potter': 86,
    sam: 67
};
console.log(mathMarks);
console.log('Data type is ' + typeof mathMarks);


